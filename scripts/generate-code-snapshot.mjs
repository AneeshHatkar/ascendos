import { readdirSync, readFileSync, statSync, writeFileSync } from "node:fs";
import { extname, join, relative } from "node:path";

const ROOT = process.cwd();
const OUTPUT_FILE = "CODE_SNAPSHOT.md";

const INCLUDED_EXTENSIONS = new Set([
  ".ts",
  ".tsx",
  ".js",
  ".mjs",
  ".json",
  ".md",
  ".css",
  ".sql",
  ".toml",
]);

const EXCLUDED_DIRS = new Set([
  ".git",
  ".next",
  ".venv",
  "node_modules",
  "out",
  "dist",
  "build",
  "coverage",
]);

const EXCLUDED_FILES = new Set([
  OUTPUT_FILE,
  "package-lock.json",
]);

function shouldIncludeFile(path) {
  const name = path.split("/").pop();

  if (!name || EXCLUDED_FILES.has(name)) {
    return false;
  }

  if (name.startsWith(".env") && name !== ".env.example") {
    return false;
  }

  return INCLUDED_EXTENSIONS.has(extname(path));
}

function walk(dir) {
  const entries = readdirSync(dir).sort();
  const files = [];

  for (const entry of entries) {
    const absolutePath = join(dir, entry);
    const relativePath = relative(ROOT, absolutePath);

    if (EXCLUDED_DIRS.has(entry)) {
      continue;
    }

    const stats = statSync(absolutePath);

    if (stats.isDirectory()) {
      files.push(...walk(absolutePath));
      continue;
    }

    if (stats.isFile() && shouldIncludeFile(relativePath)) {
      files.push(relativePath);
    }
  }

  return files;
}

function languageForFile(file) {
  const extension = extname(file);

  if (extension === ".ts" || extension === ".tsx") return "tsx";
  if (extension === ".js" || extension === ".mjs") return "js";
  if (extension === ".json") return "json";
  if (extension === ".md") return "md";
  if (extension === ".css") return "css";
  if (extension === ".sql") return "sql";
  if (extension === ".toml") return "toml";

  return "";
}

const files = walk(ROOT);

const lines = [
  "# ascendOS Code Snapshot",
  "",
  "This file is generated by `npm run snapshot:code`.",
  "It exists so a future chat can understand the current codebase quickly.",
  "",
  "## How to use in a new chat",
  "",
  "Upload or paste this file and say:",
  "",
  "> Read this CODE_SNAPSHOT.md, then continue the ascendOS + Carnos implementation according to the DOCX/JSON source-of-truth.",
  "",
  "## Included files",
  "",
];

for (const file of files) {
  lines.push(`- \`${file}\``);
}

lines.push("", "## File Contents", "");

for (const file of files) {
  const language = languageForFile(file);
  const content = readFileSync(file, "utf8");

  lines.push(`### \`${file}\``, "");
  lines.push(`\`\`\`${language}`);
  lines.push(content.trimEnd());
  lines.push("```", "");
}

writeFileSync(OUTPUT_FILE, `${lines.join("\n")}\n`);

console.log(`Generated ${OUTPUT_FILE} with ${files.length} files.`);
